# Konversi lbs ke kg
berat_in_lbs = 50
lbs_to_kg = 0.45
berat_max = berat_in_lbs * lbs_to_kg
print("Berat max yang diperbolehkan adalah : ", berat_max, "kg")

# Kondisi a
berat_a = 115
print("Apakah berat mencukupi: ", berat_max > berat_a)

# Kondisi b
berat_b = 49
print("Apakah berat mencukupi: ", berat_max > berat_b)